OPEN_FILE PROC NEAR
  MOV AH, 3DH           
  MOV AL, 00            
  INT 21H
  JC DISPLAY_ERROR
  MOV FILE_HANDLE, AX
  RET

DISPLAY_ERROR:
  LEA DX, ERROR_STR
  MOV AH, 09
  INT 21H

EXIT2:
    MOV AX, 4C00H
    INT 21H 
OPEN_FILE ENDP
;-----------------------------------
READ_FILE PROC NEAR
  MOV AH, 3FH           
  MOV BX, FILE_HANDLE    
  MOV CX, 2000             
  INT 21H
  JC DISPLAY_ERROR2
  CMP AX, 00            
  JE DISPLAY_ERROR2

CLOSE_FILE:
  MOV AH, 3EH           
  MOV BX, FILE_HANDLE    
  INT 21H
  RET

DISPLAY_ERROR2:
  LEA DX, ERROR_STR
  MOV AH, 09
  INT 21H

EXIT3:
    MOV AX, 4C00H
    INT 21H 
READ_FILE ENDP
