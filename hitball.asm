DISP_HOME PROC NEAR 

HOME_COLOR:
   CALL SET_HOME_COLOR

HOME_POS:
   MOV DX, 0000H
     PUSH DX
   CALL SET_CURSOR

DISPLAY_HOME: 
  LEA DX, HOME_STR
  MOV AH, 09
  INT 21H

ARROW_POS:
  MOV DH, ARROW_ROW
  MOV DL, ARROW_COL
  PUSH DX
  CALL SET_CURSOR

DISPLAY_ARROW:
  MOV AH, 09H
  LEA DX, ARROW
  INT 21H

CHOOSE:   
  MOV AH, 00H   
  INT 16H
  CMP AL, 0DH   
  JE  CHOICE
  CMP AH, 4BH   
  JE  LEFT
  CMP AH, 4DH 
  JE  RIGHT

  JMP CHOOSE

RIGHT:    
  ;CALL    BEEP_1
  CMP ARROW_COL, 46   ;IF RIGHT KEY
  JE  CHOOSE
  MOV DH, ARROW_ROW
  MOV DL, ARROW_COL
  PUSH DX
  CALL SET_CURSOR
  MOV AH, 09H
  LEA DX, SPACE
  INT 21H
  ;CALL DISPLAY
  ADD ARROW_COL, 17
  MOV DH, ARROW_ROW
  MOV DL, ARROW_COL
  PUSH DX
  CALL SET_CURSOR
  MOV AH, 09H
  LEA DX, ARROW
  INT 21H
  JMP CHOOSE

LEFT: 
  ;CALL    BEEP_1 
  CMP ARROW_COL, 12   ;IF LEFT KEY
  JE CHOOSE
  MOV DH, ARROW_ROW
  MOV DL, ARROW_COL
  PUSH DX
  CALL SET_CURSOR
  MOV AH, 09H
  LEA DX, SPACE
  INT 21H
  ;CALL DISPLAY
  SUB ARROW_COL, 17
  MOV DH, ARROW_ROW
  MOV DL, ARROW_COL
  PUSH DX
  CALL SET_CURSOR
  MOV AH, 09H
  LEA DX, ARROW
  INT 21H
  JMP CHOOSE

CHOICE:   
  CMP ARROW_COL, 12   ;START GAME
  JE PLAY_GAME
  CMP ARROW_COL, 29
  JE DISPLAY_HOW
  CMP ARROW_COL, 46
  JE FIN
  JMP CHOOSE

PLAY_GAME:
  POP DX
  MOV BX, 0
  PUSH BX
  PUSH DX
  RET

DISPLAY_HOW:    
  POP DX
  MOV BX, 1
  PUSH BX
  PUSH DX
  RET
  
FIN:    
  ;CALL    BEEP_2
  MOV AX, 4C00H
    INT 21H 
    RET

  RET
DISP_HOME ENDP
