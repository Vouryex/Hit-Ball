	MOV AL, BALL1_ROW
	DEC AL
	MOV BALL1_ROW, AL
	JMP IS_BALL2_THROWN_UP

IS_BALL1_HIT_PLAYER2_LEFT_BOUND:
	MOV AL, PLAYER2_BODY_COL
	MOV AH, BALL1_COL
	CMP AL, AH
	JBE IS_BALL1_HIT_PLAYER2_RIGHT_BOUND
	JMP STOP_BALL1

IS_BALL1_HIT_PLAYER2_RIGHT_BOUND:
	ADD AL, 3
	CMP AL, AH
	JA BALL1_HIT_PLAYER2
	JMP STOP_BALL1

