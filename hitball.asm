MAIN PROC FAR
	MOV AX, @data
	MOV DS, AX
 	MOV ES, AX

INIT_HOME_DISPLAY:
	LEA DX, HOME_FILE
	CALL OPEN_FILE

	LEA DX, HOME_STR
	CALL READ_FILE

INIT_HOW_DISPLAY:
	LEA DX, HOW_FILE
	CALL OPEN_FILE

	LEA DX, HOW_STR
	CALL READ_FILE

MENU:
 	CALL DISP_HOME
 	POP BX

IS_CHOICE_GAME:
	CMP BX, 0
	JE GAME
	JMP HOW_TO

GAME:
	CALL PLAY
	JMP MENU

HOW_TO:
	CALL DISP_HOWTO
	JMP MENU

EXIT:
   	MOV   AX, 4C00H
    INT   21H
