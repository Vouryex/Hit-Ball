GET_NUMERICAL MACRO STRING, NUMERICAL
        LOCAL ITERATE
        LOCAL EQUAL
    
       	MOV NUMERICAL, 0
		MOV MULTIPLY_TEN, 1

        STD               
        MOV CX, 0002H          
        LEA SI, STRING
        ADD SI, CX
        DEC SI

ITERATE:
        MOV AX, 0
        MOV AL, [SI]
        SUB AX, 48
        MOV BX, 0
        ADD BL, MULTIPLY_TEN
        MUL BX

        MOV BX, 0
        ADD BL, NUMERICAL
        ADD AX, BX
        MOV NUMERICAL, AL

        MOV AX, 0
        ADD AL, MULTIPLY_TEN
        MOV BX, 10
        MUL BX
        MOV MULTIPLY_TEN, AL

        DEC SI
        LOOP ITERATE

ENDM
